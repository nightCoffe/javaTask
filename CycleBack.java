
/*
создать переменную счетчик
создать цикл вайл
добавить условие
в цикле вычесть у счетчика единицу
 */


public class CycleBack {
    public static void main(String[] args) {
        int count = 10;
        while (count >= 1) {
            System.out.println(count);
            count--;
        }
    }
}
